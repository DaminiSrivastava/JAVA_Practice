package Condition_Statement;

public class switch_case {

	public static void main(String[] args) {
		String ch ="Damini";
	   switch(ch){
	   case "Damini":
	  System.out.println("damini");
	   break;
	   case "sr":
	  System.out.println("Srivastava");
	   break;
	   default:System.out.println("nothing");
	   }
		

	}

}
